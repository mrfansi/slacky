interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  triggerClassName?: string;
}

export function EmojiPicker({ onEmojiSelect, triggerClassName }: EmojiPickerProps) {
  const [open, setOpen] = useState(false);
  const [activeGroup, setActiveGroup] = useState(0);
  
  const handleEmojiSelect = (emoji: string) => {
    onEmojiSelect(emoji);
    setOpen(false);
  };
  
  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button 
          variant="ghost" 
          size="sm" 
          className={triggerClassName}
        >
          <Smile className="h-4 w-4" />
        </Button>
      </PopoverTrigger>
      <PopoverContent 
        className="w-64 p-2 border-none" 
        side="top" 
        align="end"
        sideOffset={5}
      >
        <div className="flex flex-col gap-2">
          <div className="flex justify-between border-b pb-2">
            {EMOJI_GROUPS.map((group, index) => (
              <Button 
                key={group.name} 
                variant="ghost" 
                size="sm"
                className={cn(
                  "p-1",
                  activeGroup === index && "bg-muted"
                )}
                onClick={() => setActiveGroup(index)}
              >
                {group.emojis[0]}
              </Button>
            ))}
          </div>
          
          <div className="grid grid-cols-8 gap-1 h-[200px] overflow-y-auto">
            {EMOJI_GROUPS[activeGroup].emojis.map((emoji) => (
              <Button 
                key={emoji} 
                variant="ghost" 
                size="sm"
                className="p-1 h-8 w-8"
                onClick={() => handleEmojiSelect(emoji)}
              >
                {emoji}
              </Button>
            ))}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  );
}
